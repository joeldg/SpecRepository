async function getJson(target: FastifyInstance, url: string) {
  const res = await target.inject({ method: "GET", url });
  expect(res.statusCode).toBe(200);
  return res.json();
}

async function findSpec(filename: string, typeName: string) {
  const specs = await getJson(app, "/api/v1/specs");
  return specs.find((s: any) => s.filename === filename && s.project_type_name === typeName);
}

async function submitCr(specId: string, content: string, delta = "minor") {
  const res = await app.inject({
    method: "POST",
    url: "/api/v1/specs/review",
    payload: { spec_id: specId, proposed_content: content, version_delta: delta, proposed_by: "joel" },
  });
  expect(res.statusCode).toBe(201);
  return res.json();
}

