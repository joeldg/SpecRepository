describe("inbound GitHub sync", () => {
  it("verifies the HMAC and opens a change request for edited spec files", async () => {
    process.env.GITHUB_WEBHOOK_SECRET = "gh-secret";
    const types = await getJson(app, "/api/v1/project-types");
    const edge = types.find((t: any) => t.name === "Acme Edge Device");
    await app.inject({
      method: "POST",
      url: "/api/v1/subscriptions",
      payload: { project_type_id: edge.id, repo: "joeldg/edge-repo", branch: "main", base_path: "specs" },
    });

    const newContent = "# Acme Edge Device — Design Specification\n\nEdited in the repo.\n";
    vi.stubGlobal(
      "fetch",
      vi.fn().mockResolvedValue(new Response(newContent, { status: 200 }))
    );

    const event = JSON.stringify({
      ref: "refs/heads/main",
      repository: { full_name: "joeldg/edge-repo" },
      pusher: { name: "joel" },
      head_commit: { id: "abc123" },
      commits: [{ added: [], modified: ["specs/DESIGN.md", "src/main.c"] }],
    });
    const signature = "sha256=" + crypto.createHmac("sha256", "gh-secret").update(event).digest("hex");

    const forged = await app.inject({
      method: "POST",
      url: "/api/v1/integrations/github/webhook",
      headers: { "content-type": "application/json", "x-github-event": "push", "x-hub-signature-256": "sha256=bad" },
      payload: event,
    });
    expect(forged.statusCode).toBe(401);

    const res = await app.inject({
      method: "POST",
      url: "/api/v1/integrations/github/webhook",
      headers: {
        "content-type": "application/json",
        "x-github-event": "push",
        "x-hub-signature-256": signature,
      },
      payload: event,
    });
    expect(res.statusCode).toBe(200);
    expect(res.json().change_requests.length).toBe(1);

    const reviews = await getJson(app, "/api/v1/reviews?status=pending");
    expect(reviews[0].proposed_by).toContain("joel (via joeldg/edge-repo)");
    expect(reviews[0].filename).toBe("DESIGN.md");
    // Removing sections suggests a major bump, picked automatically for inbound changes
    expect(JSON.parse(reviews[0].compatibility).suggested_delta).toBe("major");
  });
});

