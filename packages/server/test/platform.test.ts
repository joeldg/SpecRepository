    const zip = new AdmZip(dl.rawPayload);
    expect(zip.readAsText("DESIGN.md")).toContain("BETA");

    const promoted = await app.inject({
      method: "POST",
      url: `/api/v1/specs/${spec.id}/promote`,
      payload: { version: "1.1.0-beta.1", promoted_by: "release-mgr" },
    });
